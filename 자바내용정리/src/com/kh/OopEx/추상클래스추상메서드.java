package com.kh.OopEx;
/*
 추상 클래스 : 하나 이상의 추상 메서드를 포함할 수 있는 클래스로,
  		   추상 메서드를 만들어주어야 하고 객체를 직접 생성할 수 없기 때문에, 
  		   추상 클래스를 상속받는 하위 클래스에서 추상 메서드를 작성해야지만 사용할 수 있다.
  		   추상 클래스는 하나 이상의 추상 메서드를 포함할 수 있다.
  		   생성자 메서드도 만들 수 있기는 하지만, 만야 생성자 메서드를 만들었다면 
  		   반드시 자식 클래스에서 생성자 또한 사용을 해주어야 한다.
  		   default 까지는 사용이 가능하지만, private 을 사용하면 에러가 발생한다.
  		   
  		   public abstract class 클래스이름 {
  		   
  		   }
  		   
  		   추상 클래스를 자식 클래스에서 사용하길 원한다면, extends 를 사용하면 된다.
  		   
 추상 메서드 : 추상 메서드는 인터페이스와 마찬가지로 메서드 이름까지만 존재하고,
  		   본문은 존재하지 않는 메서드이다.
 */
public abstract class 추상클래스추상메서드 {
	abstract void 추상메서드();
}
