package com.kh.SwitchEx;
/*
 Switch - Case : 어떤 값이 switch 안으로 주어지고, 주어진 값 중에 일치하는 값이 case 에 있다면,
  				 case 에서 어떤 특정 행동을 진행할 수 있도록 설정할 수 있다.
  				 주어진 값이 일치하는 case 가 없다면 default 로 이동하게 된다.
  				 case 는 break 가 없으면 나머지 밑에 있는 case 와 default 에 작성된 코드들이 모두 실행되기 때문에,
  				 case 에 필요한 기능을 작성하고, break 를 작성해주는게 좋다.
  				 default 는 필수로 작성하지 않아도 된다.
  				 
 		switch(변수 또는 표현식) {
 			case 값1 : 
 				변수가 값1 에 해당하는 내용일 때 실행할 코드 작성
 				break; // 필수는 아니지만 작성해주는게 좋다.
 			case 값2 : case 값3 : 
 				변수가 값2와 값3 중 하나와 일치할 때 실행할 코드 작성
 				break;
 			default : 
 				모든 case 와 일치하지 않을 때 실행할 코드 작성
 				break;
 		}
 */
